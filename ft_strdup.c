#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	i;
	size_t	x;
	char	*m;

	i = 0;
	x = ft_strlen(s1);
	m = (char *)malloc(x + 1);
	if (m == NULL)
		return (NULL);
	while (i < x)
	{
		m[i] = s1[i];
		++i;
	}
	if (i == x)
		m[i] = '\0';
	return (m);
}

